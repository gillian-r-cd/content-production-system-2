    print(f"   模型:   {display_model}")
    print(f"   温度:   {TEST_TEMPERATURE}")
    print(f"   Prompt: {len(SYSTEM_PROMPT)} chars (system)")
    print(f"   User:   {len(USER_MESSAGE)} chars")
    print("=" * 70)

    # ── 2. 构造消息 ──
    messages = [
        SystemMessage(content=SYSTEM_PROMPT),
        HumanMessage(content=USER_MESSAGE),
    ]

    # ── 3. 调用 LLM（流式输出到终端） ──
    print("\n📤 发送请求...")
    start_time = time.time()

    full_content = ""
    try:
        # 流式输出：实时打印 token
        print("\n--- LLM 输出开始 ---\n")
        async for chunk in llm.astream(messages):
            token = chunk.content or ""
            if token:
                print(token, end="", flush=True)
                full_content += token
        print("\n\n--- LLM 输出结束 ---")
    except Exception as e:
        from core.llm import parse_llm_error
        print(f"\n\n❌ 调用失败: {parse_llm_error(e)}")
        print(f"   原始错误: {e}")
        return

    elapsed = time.time() - start_time

    # ── 4. 统计信息 ──
    print(f"\n⏱  耗时: {elapsed:.1f}s")
    print(f"📊 输出长度: {len(full_content)} chars")

    # ── 5. 保存到文件 ──
