async def run_ab_test():
    """
    A/B 对比测试：用同一套 prompt 分别调用两个不同模型，对比输出质量。
    """
    from core.llm import get_chat_model
    from core.llm_compat import get_model_name

    # 配置：要对比的两个模型（修改这里）
    MODEL_A = TEST_MODEL or get_model_name()
    MODEL_B = None  # 设为 None 则跳过 B 模型；否则填写模型名如 "gpt-5.1"

    models = [MODEL_A]
    if MODEL_B:
        models.append(MODEL_B)

    results = {}

    for model_name in models:
        print(f"\n{'=' * 70}")
        print(f"🔬 测试模型: {model_name}")
        print(f"{'=' * 70}")

        llm = get_chat_model(
            model=model_name,
            temperature=TEST_TEMPERATURE,
            streaming=True,
        )

        messages = [
            SystemMessage(content=SYSTEM_PROMPT),
            HumanMessage(content=USER_MESSAGE),
        ]

        start_time = time.time()
        full_content = ""
        try:
            async for chunk in llm.astream(messages):
                token = chunk.content or ""
                if token:
                    print(token, end="", flush=True)
                    full_content += token
        except Exception as e:
            from core.llm import parse_llm_error
            print(f"\n❌ {model_name} 调用失败: {parse_llm_error(e)}")
            continue

        elapsed = time.time() - start_time
        results[model_name] = {
            "content": full_content,
            "elapsed": elapsed,
            "length": len(full_content),
        }
        print(f"\n\n⏱  {model_name}: {elapsed:.1f}s, {len(full_content)} chars")

    # 保存 A/B 对比结果
    if results:
        data_dir = os.path.join(_SCRIPT_DIR, "data")
        os.makedirs(data_dir, exist_ok=True)

        timestamp = time.strftime("%Y%m%d_%H%M%S")
        filepath = os.path.join(data_dir, f"jp_ab_test_{timestamp}.md")

        with open(filepath, "w", encoding="utf-8") as f:
            f.write(f"# 日语本地化 A/B 测试结果\n\n")
            f.write(f"- **时间**: {time.strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write(f"- **温度**: {TEST_TEMPERATURE}\n\n")

            for model_name, result in results.items():
                f.write(f"---\n\n## 模型: {model_name}\n\n")
                f.write(f"- 耗时: {result['elapsed']:.1f}s\n")
                f.write(f"- 输出长度: {result['length']} chars\n\n")
                f.write(result["content"])
                f.write("\n\n")

        print(f"\n💾 A/B 对比结果已保存: {filepath}")


